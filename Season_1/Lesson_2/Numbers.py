# Functions to work with numbers
first_number = 6
second_number = 4
# Perform an addition of two numbers
print(first_number + second_number)
# Perform a subtraction of two numbers
print(first_number - second_number)
# Perform a multiplication of two numbers
print(first_number * second_number)
# Perform a division of two numbers
print(first_number / second_number)
# Make the one number as an exponent of the other
print(first_number ** second_number)

# Caution by printing strings and numbers together
# You have to convert the number into a string
